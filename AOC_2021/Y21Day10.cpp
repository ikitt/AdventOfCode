Y21Day10::Y21Day10()
{

}


void Y21Day10::computFirstResult()
{
    unsigned int res = 0;
    for(QString line : _input)
    {
        line = line.simplified();
        QVector<QChar> openedChunk;
        int idx = -1;
        for(QChar chunkCh : line)
        {
            idx++;
            if(_openChunk.contains(chunkCh))
            {
                openedChunk.push_back(chunkCh);
            }
            else if(_closeChunk.contains(chunkCh))
            {
                if(_closingMap[openedChunk.last()] == chunkCh)
                {
                    openedChunk.pop_back();
                }
                else
                {
//                    fprintf(stdout, "Line %s got ", line.toStdString().c_str());
//                    fprintf(stdout, "the wrong close chunk %c (at %i)\r\n", chunkCh.toLatin1(),idx);
                    fflush(stdout);
                    res += _pointMap[chunkCh.toLatin1()];
                    break;
                }
            }
            else
            {
                throw std::domain_error("Unknown char");
            }
        }
    }

    fprintf(stdout, "Res for first part = %i\r\n", res);
    fflush(stdout);
}


void Y21Day10::computSecondResult()
{

    QVector<unsigned long long int> scoreVect;
    unsigned long long int res = 0;
    for(QString line : _input)
    {
        line = line.simplified();
        unsigned long long int lineScore = 0;
        QVector<QChar> openedChunk;
        int idx = -1;
        for(QChar chunkCh : line)
        {
            idx++;
            if(_openChunk.contains(chunkCh))
            {
                openedChunk.push_back(chunkCh);
            }
            else if(_closeChunk.contains(chunkCh))
            {
                if(_closingMap[openedChunk.last()] == chunkCh)
                {
                    openedChunk.pop_back();
                }
                else
                {
                    break;
                }
            }
            else
            {
                throw std::domain_error("Unknown char");
            }
        }

        while(openedChunk.size() >0)
        {
            fprintf(stdout, "Add %i to %i due to %c\r\n", ((lineScore*5) + _pointIncomplete[openedChunk.last().toLatin1()]), lineScore, openedChunk.last().toLatin1());
            fflush(stdout);

            lineScore = ((lineScore*5) + _pointIncomplete[openedChunk.last().toLatin1()]);
            openedChunk.pop_back();
        }
        scoreVect.push_back(lineScore);
    }

    std::sort(scoreVect.begin(),scoreVect.end());

    int size = scoreVect.size();


    fprintf(stdout, "Res for second part %llu (size %i)\r\n", scoreVect[(size+1)/2], scoreVect.size());
    fflush(stdout);
}


